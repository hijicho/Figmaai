【理学部一覧ページの要件】
- ページタイトルは「文学部一覧ページ」
- 既存の教員一覧ページと同じ見せ方にする
- 一覧は「探して押すページ」と一目で分かる、シンプルな構成にする
- 罫線、セル余白、行間、文字サイズは既存デザインに合わせる
- 各授業名または教員名は青文字のリンク風デザインにする
- 同じ科目名でも担当教員が違う場合は別項目として表示する

【追加してほしい機能】
- 文学部ページでのみ使用可能な検索欄を、画面中央付近の分かりやすい位置に設ける
- 検索対象は「授業名」「担当教員」
- 一覧ページと詳細ページに存在する「授業名」「担当教員」の情報を横断して検索できる想定にする
- 特に「授業名」「担当教員」はすべて単語レベルで検索可能なUI/UXを前提にする
- 検索語を入力すると、該当する授業の一覧候補が表示される
- 検索結果は一覧形式で見せ、押すと該当の授業詳細ページへ遷移できる
- オートコンプリートまたは候補サジェストが自然に見えるUIにする
- 文学部ページ専用機能であることが分かるようにしつつ、既存UIから浮かない見た目にする
-デザインは法学部のものと同じでお願いします。

【詳細ページの要件】
- 既存の授業詳細ページのトーンに合わせる
- 一覧ページから自然に遷移する
- 戻るリンクで法学部一覧ページに戻れる
- 詳細ページ内にも授業名・担当教員が分かりやすく表示される
- プロトタイプ上で一覧→詳細→一覧の戻り導線を確認できるようにする

【デザインの方向性】
- 既存サービスのUIに忠実に合わせる
- 現代システム科学域ページと同じ導線・情報設計を踏襲する
- 情報量は整理されていて、視認性を優先する
- 学生が授業を探しやすいことを最優先にする
- 過度に装飾せず、既存サービス内で違和感のないデザインにする

