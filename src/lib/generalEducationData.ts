// 曜日と時限で授業をソート
export const sortedCourses = [...generalEducationCourses].sort((a, b) => {
  const dayOrder: Record<string, number> = {
    '月曜日': 0,
    '火曜日': 1,
    '水曜日': 2,
    '木曜日': 3,
    '金曜日': 4,
    '時間割外': 5,
  };
  
  const periodOrder: Record<string, number> = {
    '１限目': 1,
    '２限目': 2,
    '３限目': 3,
    '４限目': 4,
    '５限目': 5,
  };
  
  const dayA = dayOrder[a.day] ?? 999;
  const dayB = dayOrder[b.day] ?? 999;
  
  if (dayA !== dayB) {
    return dayA - dayB;
  }
  
  const periodA = periodOrder[a.period] ?? 999;
  const periodB = periodOrder[b.period] ?? 999;
  
  return periodA - periodB;
});
